class Productos {
    constructor(Id, Nombre, Marca, Mascota, Categoria, TamañoMascota, Edad, Peso, Precio, Stock){
        this.Id = Id;
        this.Nombre = Nombre;
        this.Marca = Marca;
        this.Mascota = Mascota;
        this.Categoria = Categoria;
        this.TamañoMascota = TamañoMascota;
        this.Edad = Edad;
        this.Peso = Peso;
        this.Precio = Precio;
        this.Stock = Stock;
    }
}

module.exports = Productos;
